# Introduction to Python Syntax

# This is a comment in Python. Comments are ignored by the interpreter.
# Use comments to explain what your code does.

# Printing output
print("Hello, World!")  # This line prints "Hello, World!" to the console.

# Variables and Data Types
name = "Alice"  # String variable
age = 30  # Integer variable
height = 5.5  # Float variable
is_student = True  # Boolean variable

# Displaying variable values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

# Basic Arithmetic Operations
a = 10
b = 5

print("Addition:", a + b)           # Addition
print("Subtraction:", a - b)        # Subtraction
print("Multiplication:", a * b)     # Multiplication
print("Division:", a / b)           # Division (returns float)
print("Floor Division:", a // b)    # Floor Division (returns integer)
print("Modulus:", a % b)            # Modulus (returns remainder)
print("Expotentiation:", a ** b)    # Exponentiation (power)

# Comparison Operators
print("Equal to:", a == b)                  # Equal to
print("Not equal to:", a != b)              # Not equal to
print("Greater than:", a > b)               # Greater than
print("Less than:", a < b)                  # Less than
print("Greater than or equal to:", a >= b)  # Greater than or equal to
print("Less than or equal to:", a <= b)     # Less than or equal to

# Logical Operators
x = True
y = False

print("AND:", x and y)  # Logical AND
print("OR:", x or y)    # Logical OR
print("NOT:", not x)    # Logical NOT

# Taking Input from User
user_name = input("Enter your name: ")  # Takes input from the user
print("Hello, " + user_name + "!")
